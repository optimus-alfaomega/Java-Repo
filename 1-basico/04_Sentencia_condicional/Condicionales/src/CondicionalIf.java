public class CondicionalIf {
    public static void main(String args[]) {
        int edad = 17;
        
        if (edad >= 18) {
            System.out.println("Eres mayor de edad");          
        }
        
        if (edad < 18) {
            System.out.println("Eres menor de edad");          
        }
        
        
    }
}
